//HSI(8MHz)を使用
//PLL x18により
//SYSCLK　144MHz
//HCLK 144MHz
//APB1 144MHz
//APB2 144MHz
//ADC 72MHz
//(状況により変更あり)
void osc_144MHz_init(void) {
    //PLL x18に設定
    RCC->CFGR0 |= (1 << 21);
    RCC->CFGR0 |= (1 << 20);
    RCC->CFGR0 |= (1 << 19);
    RCC->CFGR0 |= (1 << 18);

    //PLL HSI->そのまま入力 (/2しない)
    EXTEN->EXTEN_CTR |= (1 << 4);

    //PLL有効　(input clock x 18)
    RCC->CTLR |= (1 << 24);

    //PLLがReadyになるまで待機
    while((RCC->CTLR & (1 << 25)) == 0);

    //システムクロックをPLL入力にセット
    RCC->CFGR0 |= (1 << 1);
}

void SysTick_init(void) {
    //STRE オートリロード有効 (最大値になると0にもどる）
    SysTick->CTLR |= (1 << 3);

    //HCLKをそのまま入力として使う
    //HCLK/8 0にするとHCLK/8になる
    SysTick->CTLR |= (1 << 2);

    SysTick->CMP = 0xFFFFFFFFFFFFFFFF;

    //SysTickクロック開始
    SysTick->CTLR |= (1 << 0);
}

//us単位のdelay生成（若干の誤差あり注意）
void us_delay(volatile uint32_t us) {
    SysTick->CNT = 0;
    while(SysTick->CNT < (us * 144));
}

void row_out(int val1, int val2, int val3, int val4) {
    digitalWrite(ROW_SRCLK, 0); //srclkを下げる
    digitalWrite(ROW1_SERIAL, val1); //SERIALにデータを入れる
    digitalWrite(ROW2_SERIAL, val2);
    digitalWrite(ROW3_SERIAL, val3);
    digitalWrite(ROW4_SERIAL, val4);
    us_delay(SHIFT_DELAY); //74HC595 データをシフトする前にちょっと待つ
    digitalWrite(ROW_SRCLK, 1); //srclkを1にしてシフト
}

//TIM2 割り込み設定 (1ms)
void TIM2_interrupt_init(void) {
    //TIM2有効化
    RCC->APB1PCENR |= RCC_APB1Periph_TIM2;
    TIM2->CTLR1 |= TIM_ARPE; //Auto-reload and Preload 有効

    TIM2->PSC = 143; //(@144MHz)
    TIM2->ATRLR = 999; //1MHzを1000Hzで割り込み

    TIM2->DMAINTENR |= TIM_UIE; //Update Interrupt 有効

    NVIC_EnableIRQ(TIM2_IRQn); //interrupt TIM2_IRQn 有効

    TIM2->CTLR1 |= TIM_CEN; //Count開始
}

void TIM2_IRQHandler(void) {
    TIM2->INTFR &= ~TIM_UIE; //interrupt解除

    static int cnt0 = 0;
    cnt0++;
    static int one_Latch = 0;
    //q3秒
    if(cnt0 > 3000){

        if(one_Latch == 0){
            one_Latch = 1;

            for(int tmp1 = 0; tmp1 < 32;tmp1++){
                for(int tmp2 = 0; tmp2 < 128; tmp2++){
                    disp_data[tmp1][tmp2] = data1[tmp1][tmp2];
                }
            }
        }

        //q6秒
        if(cnt0 > 6000){
            cnt0 = 0;
            one_Latch = 0;

            for(int tmp1 = 0; tmp1 < 32;tmp1++){
                for(int tmp2 = 0; tmp2 < 128; tmp2++){
                    disp_data[tmp1][tmp2] = data2[tmp1][tmp2];
                }
            }
        }
    }
}
